#include <stdio.h>

int main()
{
    int a = 10;
    int b = 30;

    printf("a>b =%d\n", a > b);    // true or false
    printf("a<b =%d\n", a < b);    // true or false
    printf("a>=b = %d\n", a >= b); // true or false
    printf("a<=b = %d\n", a <= b); // true or false
    printf("!a=%d\n", !a);         // true or false
    printf("!b=%d\n", !b);         // true or false
    return 0;
}
